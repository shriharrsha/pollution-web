function BO() 
{
  var progressbar = document.getElementById("pollution");
  var total = 65;
  console.log(total);
  progressbar.style.backgroundColor = "green";
  progressbar.style.width = (total-13) * 1 + "%";
  progressbar.innerText = parseInt((total) * 1) + "db";
}
